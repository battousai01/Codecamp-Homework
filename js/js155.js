let [first, second, third] = ['Maya', 'Marisa', 'Chi'];
console.log(first); // *Maya
console.log(second); // **Marisa
console.log(third); // ***Chi



/*array ['Maya', 'Marisa', 'Chi'] is being destructured into three variables: first, second, and third.
    first: The first element of the array, 'Maya', is assigned to the variable first.
    second: The second element of the array, 'Marisa', is assigned to the variable second.
    third: The third element of the array, 'Chi', is assigned to the variable third.*/